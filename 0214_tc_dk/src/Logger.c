void initUartLogger(void) {
    // UART 설정 구조체
    IfxAsclin_Asc_Config ascConfig;

    // 기본 설정으로 초기화
    IfxAsclin_Asc_initModuleConfig(&ascConfig, &MODULE_ASCLIN3);

    // 통신 속도 설정
    ascConfig.baudrate.baudrate = UART_BAUDRATE;

    // 버퍼 설정
    ascConfig.txBuffer = &ascTxBuffer;
    ascConfig.txBufferSize = ASC_TX_BUFFER_SIZE;
    ascConfig.rxBuffer = &ascRxBuffer;
    ascConfig.rxBufferSize = ASC_RX_BUFFER_SIZE;

    // Pins 설정
    const IfxAsclin_Asc_Pins pins = {
        .cts        = NULL_PTR,                   // CTS pin not used
        .ctsMode    = IfxPort_InputMode_pullUp,
        .rx         = &IfxAsclin3_RXD_P32_2_IN ,  // RX pin
        .rxMode     = IfxPort_InputMode_pullUp,
        .rts        = NULL_PTR,                   // RTS pin not used
        .rtsMode    = IfxPort_OutputMode_pushPull,
        .tx         = &IfxAsclin3_TX_P15_7_OUT,  // TX pin
        .txMode     = IfxPort_OutputMode_pushPull,
        .pinDriver  = IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConfig.pins = &pins;

    // UART 초기화
    IfxAsclin_Asc_initModule(&uart, &ascConfig);
}


void sendDataLog(const char* data) {
    IfxAsclin_Asc_write(&uart, data, (Ifx_SizeT)strlen(data), TIME_INFINITE);
    // 개행 문자 추가
    const char newline[] = "\r\n";
    IfxAsclin_Asc_write(&uart, newline, (Ifx_SizeT)strlen(newline), TIME_INFINITE);
}


void logNumericData(const char* label, sint32 value) {
    char buffer[50];
    sprintf(buffer, "%s: %.2f", label, value);
    sendDataLog(buffer);
}
